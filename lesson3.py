# Lesson 3 - Conditional execution

# Comparison operators
x = 5
if x == 5:
    print('equal than')
if x >= 5:
    print('greater than or equal')
if x > 4:
    print('greater than')
if x <= 5:
    print('less than or equal')
if x < 6:
    print('less than')
if x != 6:
    print('not equal than')

# else
if x == 5:
    print('if true')
else:
    print('else')